class Product {

    public String name;
    public double price;
    public int quantity;
    public String unit;
    public String description;

    public Product(
        String name,
        double price,
        int quantity,
        String unit,
        String description
    ) {
        this.name = name;
        this.price = price;
        this.quantity = quantity;
        this.unit = unit;
        this.description = description;
    }
}
